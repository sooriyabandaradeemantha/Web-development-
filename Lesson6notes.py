#for loop
name = 'Peter J'
for c in name:
    print(c)

#while loop
i = 1  #initialization
while i<=10: #condition checking
    print(i)
    i = i + 1 #incrementation

#nested loop
fruits = ['Plant1','Plant2','Plant3']
colors = ['W1','W2','W3']
for f in fruits:
    for c in colors:
        print(c)
    print(f)

    